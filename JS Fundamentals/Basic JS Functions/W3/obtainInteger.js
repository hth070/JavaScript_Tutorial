const randomIntegerInRange = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min;
console.log(randomIntegerInRange(0, 1000));
console.log(randomIntegerInRange(12, 75));
console.log(randomIntegerInRange(-8, 5));
console.log(randomIntegerInRange(-212, -7));


